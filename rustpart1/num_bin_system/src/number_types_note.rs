// Unsigned Integer: Can only represent positive integers

// Length of Signed Integer
// i8,     i16,    i32,    i64,    i128,   isize
// 8bit,   16bit,  32bit,  64bit,  128bit, usize

// Length of UnSigned Integer
// u8,     u16,    u32,    u64,    u128,   usize
// 8bit,   16bit,  32bit,  64bit,  128bit,     usize

// i8::MAX = 127
// i8::MIN = -128
